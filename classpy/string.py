#strings are sequence of characters enclosed within single or double or triple quotes
a="Hello CSE"
b="How are you?"
print(a)      #print entire string
print(a[0:4]) #print first four characters and colon is called "slice operator"
print(a[5])   #print sixth character
print(a*2)    #print the string 2times
print(a+"\n"+b) #concatenate two strings 